    {
        $request->validate([
            "title" => 'required',
        ]);

        $activity = ActivityType::create([
            'title'      => $request->title,
            'visibility' => $request->visibility,
            'order'      => $request->order,
            'image'      => $request->image,
            'active'     => $request->active,
        ]);

        return redirect()->route('activities-types.index')->with('message','Tipo de atividade criada com sucesso!');
    }

